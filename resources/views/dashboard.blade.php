                    <h4>Data Klustering yang digunakan</h4>
                    <p><i class="fa fa-user"></i> Hamudi &nbsp;|&nbsp; <i class="fa fa-calendar"></i> 25.10.2023 13:50 &nbsp;|&nbsp; <i class="fa fa-comments"></i>  125 comments</p>
                    Pada klustering kali ini kami menggunakan data berupa teks dari tweets dengan topik palestina, dimana topik ini sedang ramai diperbincangkan sehingga menjadikan salah satu alasan kami untuk menggunakan topik ini
                    Klustering dalam konteks teks Palestina adalah teknik analisis data yang digunakan untuk mengelompokkan teks atau dokumen berdasarkan kesamaan karakteristik atau topik yang ada dalam teks tersebut. Alasan untuk melakukan klustering dalam konteks Palestina dapat mencakup:
                    <ol>
                        <li>1. Identifikasi Pola: Klustering memungkinkan kita untuk mengidentifikasi pola dan kelompok tematik dalam teks terkait Palestina, seperti topik-topik utama yang sering muncul dalam diskusi online atau jenis peristiwa yang paling dibahas.</li><br>
                        <li>2. Pemahaman Sentimen: Dengan klustering, kita dapat memahami sentimen yang terkait dengan topik Palestina, termasuk apakah teks-teks tersebut cenderung positif, negatif, atau netral. Ini dapat membantu dalam pemantauan opini publik terkait konflik atau isu-isu terkait Palestina.</li><br>
                        <li>3. Segmentasi Kelompok Pembicara: Klustering juga memungkinkan kita untuk mengidentifikasi kelompok-kelompok atau aktor-aktor yang berpartisipasi dalam percakapan online tentang Palestina. Hal ini bisa bermanfaat untuk mengidentifikasi pemimpin opini atau kelompok-kelompok yang memiliki pandangan serupa.</li><br>
                        <li>4. Fokus Pada Isu Khusus: Klustering memungkinkan peneliti atau analis untuk memusatkan perhatian pada isu-isu khusus yang mungkin menjadi sorotan dalam pembicaraan tentang Palestina. Ini dapat membantu dalam pemahaman mendalam terhadap isu-isu yang relevan.</li><br>
                        <li>5. Meningkatkan Manajemen Informasi: Dalam konteks berita atau media sosial, klustering dapat digunakan untuk mengelompokkan dan mengorganisasi teks terkait Palestina, sehingga memudahkan akses dan manajemen informasi yang sangat besar.</li><br>
                        <li>6. Dalam praktiknya, klustering melibatkan penggunaan algoritma dan perangkat lunak analisis teks, seperti K-Means, DBSCAN, atau algoritma lainnya, yang dapat mengelompokkan teks-teks berdasarkan kesamaan dalam topik, kata-kata kunci, atau fitur lainnya. Dengan demikian, klustering membantu dalam menggali wawasan yang lebih dalam dari data teks yang berkaitan dengan topik Palestina.</li><br>
                    </ol>
                    

